/*
    3. If a is greater than b return 'a is greater than b' else 'a is less than b'. Try to implement it in to ways
    * using if else
    * ternary operator
*/

const a = 4;
const b = 3;

if (a > b) {
    console.log("a is greater than b");
} else {
    console.log("a is less than b");
}

a > b 
? console.log("a is greater than b")
: console.log("a is less than b");